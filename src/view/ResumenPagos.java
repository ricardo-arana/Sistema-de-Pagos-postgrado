    private void LlenarComboMaestrias() {
        MaestriaJpaController mJC = new MaestriaJpaController(entityMan.getInstance());
        List<Maestria> lista = mJC.findMaestriaEntities();
        cbMaestrias.addItem("Todas las maestrias");
        for (Maestria m : lista) {
            cbMaestrias.addItem(m);
        }
    }    
    private void LlenarComboCiclo() {
        CicloJpaController mJC = new CicloJpaController(entityMan.getInstance());
        List<Ciclo> lista = mJC.findCicloEntities();
        cbCiclo.addItem("Todas los Ciclos");
        for (Ciclo m : lista) {
            cbCiclo.addItem(m);
        }
    }
